public class Main {

    public static void main(String[] args) {
        // for
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println("For döngüsü bitti");

        //while döngüsü
        int i = 0;
        while (i < 10) {
            System.out.println(i);
            i++;
        }
        System.out.println("While döngüsü bitti");
    }
}
